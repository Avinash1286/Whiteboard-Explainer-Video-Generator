function createGeneratedSvg(input: { key: string; label?: string }): string {
  const concepts = conceptsFor(input.key, input.label);
  const symbol = symbolFor(concepts);
  return `<svg xmlns="http://www.w3.org/2000/svg" width="72" height="72" viewBox="0 0 72 72">
  <title>${titleCase(input.label || input.key)}</title>
  <g fill="none" stroke-linecap="round" stroke-linejoin="round">
    ${shapeMarkup(symbol)}
  </g>
</svg>`;
}

function ensureGeneratedAsset(input: { key: AssetKey; label?: string }): GeneratedManifestEntry {
  const manifest = loadManifest();
  const existing = manifest.entries.find((entry) => entry.assetKey === input.key);
  if (existing && existsSync(path.join(root, existing.svgPath))) return existing;

  mkdirSync(generatedSvgDir, { recursive: true });
  const svg = createGeneratedSvg(input);
  validateGeneratedSvg(svg);
  const svgPath = generatedSvgPath(input.key);
  writeFileSync(svgPath, `${svg}\n`, "utf8");

  const entry: GeneratedManifestEntry = {
    assetKey: input.key,
    id: `generated-openmoji-inspired.${slug(input.key)}`,
    provider: "generated-openmoji-inspired",
    collection: "openmoji-inspired",
    style: "color",
    label: titleCase(input.label || input.key),
    concepts: conceptsFor(input.key, input.label),
    svgPath: generatedSvgRelativePath(input.key),
    source: "deterministic-svg-generator",
    styleReference: "OpenMoji color SVG",
    license: "project-generated",
    attribution: "Generated by finalwhite",
    generatedAt: new Date().toISOString(),
  };

  writeManifest({
    ...manifest,
    entries: [...manifest.entries.filter((item) => item.assetKey !== input.key), entry],
  });
  svgCache.delete(entry.assetKey);
  return entry;
}

export function findGeneratedSvgAsset(key: AssetKey): GeneratedManifestEntry | null {
  const manifest = loadManifest();
  const entry = manifest.entries.find((item) => item.assetKey === key);
  if (!entry || !existsSync(path.join(root, entry.svgPath))) return null;
  return entry;
}

export function writeGeneratedSvgAsset(input: {
  key: AssetKey;
  label?: string;
  svg: string;
  source: GeneratedManifestEntry["source"];
  model?: string;
}): GeneratedManifestEntry {
  validateGeneratedSvg(input.svg);
  mkdirSync(generatedSvgDir, { recursive: true });
  const svgPath = generatedSvgPath(input.key);
  writeFileSync(svgPath, `${input.svg.trim()}\n`, "utf8");

  const entry: GeneratedManifestEntry = {
    assetKey: input.key,
    id: `generated-openmoji-inspired.${slug(input.key)}`,
    provider: "generated-openmoji-inspired",
    collection: "openmoji-inspired",
    style: "color",
    label: titleCase(input.label || input.key),
    concepts: conceptsFor(input.key, input.label),
    svgPath: generatedSvgRelativePath(input.key),
    source: input.source,
    styleReference: "OpenMoji color SVG",
    license: "project-generated",
    attribution: "Generated by finalwhite",
    generatedAt: new Date().toISOString(),
    model: input.model,
  };
  const manifest = loadManifest();
  const nextEntries = [...manifest.entries.filter((item) => item.assetKey !== input.key), entry];
  const source =
    nextEntries.every((item) => item.source === "ai-svg-generator")
      ? "ai-svg-generator"
      : nextEntries.every((item) => item.source === "deterministic-svg-generator")
        ? "deterministic-svg-generator"
        : "mixed";

  writeManifest({
    ...manifest,
    source,
    entries: nextEntries,
  });
  svgCache.delete(entry.assetKey);
  return entry;
}

function loadGeneratedSvg(entry: GeneratedManifestEntry): ParsedSvg {
  if (svgCache.has(entry.assetKey)) {
    const cached = svgCache.get(entry.assetKey);
    if (cached) return cached;
  }

  const filePath = path.join(root, entry.svgPath);
  const svg = readFileSync(filePath, "utf8");
  validateGeneratedSvg(svg);
  const parsed = parseSvg(svg);
  svgCache.set(entry.assetKey, parsed);
  return parsed;
}

function rough(seed: number, amount = 1.2): number {
  return Math.sin(seed * 9.713 + 5.3) * amount;
}

export function renderGeneratedSvgAsset(input: {
  key: AssetKey;
  label?: string;
  box: Box;
  progress: number;
  opacity: number;
  seed: number;
  clipId: string;
}): string {
  const entry = ensureGeneratedAsset({ key: input.key, label: input.label });
  const svg = loadGeneratedSvg(entry);
  const scale = Math.min(input.box.width, input.box.height) / 100;
  const size = 100 * scale;
  const x = input.box.x + (input.box.width - size) / 2 + rough(input.seed);
  const y = input.box.y + (input.box.height - size) / 2 + rough(input.seed + 11);
  const revealWidth = 100 * Math.max(0, Math.min(1, input.progress));
  const clipId = `${input.clipId}_generated_openmoji`;

  return `
    <defs>
      <clipPath id="${clipId}">
        <rect x="0" y="0" width="${revealWidth}" height="100"/>
      </clipPath>
    </defs>
    <g transform="translate(${x} ${y}) scale(${scale})" opacity="${input.opacity}" filter="url(#softShadow)" data-icon="${entry.id}" data-provider="${entry.provider}">
      <g clip-path="url(#${clipId})">
        <svg x="0" y="0" width="100" height="100" viewBox="${svg.viewBox}">
          ${svg.body}
        </svg>
      </g>
    </g>`;
}

export function generateSvgAsset(input: { key: AssetKey; label?: string }): GeneratedManifestEntry {
  return ensureGeneratedAsset(input);
}
