import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import type { AssetKey } from "./assetCatalog";

type Box = {
  x: number;
  y: number;
  width: number;
  height: number;
};

type ParsedSvg = {
  viewBox: string;
  body: string;
};

type GeneratedManifestEntry = {
  assetKey: AssetKey;
  id: string;
  provider: "generated-openmoji-inspired";
  collection: "openmoji-inspired";
  style: "color";
  label: string;
  concepts: string[];
  svgPath: string;
  source: "ai-svg-generator" | "deterministic-svg-generator";
  styleReference: "OpenMoji color SVG";
  license: "project-generated";
  attribution: "Generated by finalwhite";
  generatedAt: string;
  model?: string;
};

type GeneratedManifest = {
  generatedAt: string;
  provider: "generated-openmoji-inspired";
  collection: "openmoji-inspired";
  style: "color";
  source: "ai-svg-generator" | "deterministic-svg-generator" | "mixed";
  styleReference: "OpenMoji color SVG";
  total: number;
  entries: GeneratedManifestEntry[];
};

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const generatedRoot = path.join(root, "assets", "generated", "openmoji-inspired");
const generatedSvgDir = path.join(generatedRoot, "color", "svg");
const manifestPath = path.join(generatedRoot, "manifest.json");
const svgCache = new Map<string, ParsedSvg>();
let manifestCache: GeneratedManifest | undefined;

function normalize(value: string): string {
  return value
    .replace(/([a-z])([A-Z])/g, "$1 $2")
    .normalize("NFKD")
    .replace(/[^\w\s-]/g, " ")
    .replace(/_/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .toLowerCase();
}

function slug(value: string): string {
  const normalized = normalize(value).replace(/\s+/g, "-").replace(/-+/g, "-");
  return normalized || "generated-concept";
}

function titleCase(value: string): string {
  return normalize(value)
    .split(/\s+/)
    .filter(Boolean)
    .map((word) => `${word[0]?.toUpperCase() ?? ""}${word.slice(1)}`)
    .join(" ");
}

function conceptsFor(key: string, label?: string): string[] {
  return [...new Set([key, label ?? ""].flatMap((value) => normalize(value).split(/\s+/)).filter((word) => word.length > 2))];
}

function generatedSvgRelativePath(assetKey: string): string {
  return `assets/generated/openmoji-inspired/color/svg/${slug(assetKey)}.svg`;
}

function generatedSvgPath(assetKey: string): string {
  return path.join(generatedSvgDir, `${slug(assetKey)}.svg`);
}

function emptyManifest(): GeneratedManifest {
  return {
    generatedAt: new Date().toISOString(),
    provider: "generated-openmoji-inspired",
    collection: "openmoji-inspired",
    style: "color",
    source: "mixed",
    styleReference: "OpenMoji color SVG",
    total: 0,
    entries: [],
  };
}

function loadManifest(): GeneratedManifest {
  if (manifestCache) return manifestCache;
  if (!existsSync(manifestPath)) {
    manifestCache = emptyManifest();
    return manifestCache;
  }
  manifestCache = JSON.parse(readFileSync(manifestPath, "utf8")) as GeneratedManifest;
  return manifestCache;
}

function writeManifest(manifest: GeneratedManifest): void {
  mkdirSync(generatedRoot, { recursive: true });
  const sorted = manifest.entries.slice().sort((a, b) => a.assetKey.localeCompare(b.assetKey));
  const generatedAt = new Date().toISOString();
  const nextManifest = {
    ...manifest,
    generatedAt,
    total: sorted.length,
    entries: sorted,
  };
  writeFileSync(
    manifestPath,
    `${JSON.stringify(nextManifest, null, 2)}\n`,
    "utf8",
  );
  manifestCache = nextManifest;
}

export function validateGeneratedSvg(svg: string): void {
  const lower = svg.toLowerCase();
  if (!lower.includes("<svg") || !lower.includes("</svg>")) {
    throw new Error("Generated SVG must contain a complete <svg> document.");
  }
  if (svg.length > 40_000) {
    throw new Error("Generated SVG is too large for the asset fallback path.");
  }
  if (/<script\b/i.test(svg) || /\son[a-z]+\s*=/i.test(svg)) {
    throw new Error("Generated SVG must not contain scripts or event handlers.");
  }
  if (/\b(?:href|xlink:href)\s*=\s*["'](?:https?:|data:|file:)/i.test(svg)) {
    throw new Error("Generated SVG must not reference external or embedded images.");
  }
  if (!/\sviewBox="0 0 72 72"/i.test(svg)) {
    throw new Error('Generated SVG must use viewBox="0 0 72 72".');
  }
  if (!/stroke\s*=\s*["'](?:#?111111|#?111|#?000000|#?000|black)["']/i.test(svg)) {
    throw new Error("Generated SVG must include bold black OpenMoji-style outlines.");
  }
}

function parseSvg(svg: string): ParsedSvg {
  const viewBox = svg.match(/\sviewBox="([^"]+)"/)?.[1] ?? "0 0 72 72";
  const body = svg
    .replace(/<\?xml[\s\S]*?\?>/g, "")
    .replace(/<!doctype[\s\S]*?>/gi, "")
    .replace(/<svg\b[^>]*>/i, "")
    .replace(/<\/svg>\s*$/i, "")
    .trim();

  if (!body) {
    throw new Error("Generated SVG is empty.");
  }
  return { viewBox, body };
}

function symbolFor(concepts: string[]): string {
  const text = concepts.join(" ");
  if (/\b(?:cell|cells|biology|bio|gene|genetic|genomics|dna|epigenetic)\b/.test(text)) return "cell";
  if (/\b(?:switch|toggle|control|factor|activate|activation|reprogram)\b/.test(text)) return "switch";
  if (/\b(?:lab|medicine|drug|trial|clinical|vial|test)\b/.test(text)) return "lab";
  if (/\b(?:fund|funding|money|capital|raise|valuation|investor)\b/.test(text)) return "capital";
  if (/\b(?:ai|brain|model|intelligence|neural)\b/.test(text)) return "ai";
  if (/\b(?:team|founder|leader|people|group)\b/.test(text)) return "team";
  if (/\b(?:platform|pipeline|process|workflow|system|engine)\b/.test(text)) return "system";
  if (/\b(?:security|safe|risk|protect|shield)\b/.test(text)) return "shield";
  if (/\b(?:cloud|server|database|storage|compute)\b/.test(text)) return "infra";
  return "concept";
}

